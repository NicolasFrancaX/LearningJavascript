/**
 * Crie um objeto chamado Calc que implemente as quatro operações
 * básicas da matemática (soma, subtração, multiplicação e divisão).
 */

var Calc = {
    soma: function(n1, n2) {
      return n1 + n2;
    }
  , subtracao: function(n1, n2) {
      return n1 - n2;
    }
  , multiplicacao: function(n1, n2) {
      return n1 * n2;
    }
  , divisao: function(n1, n2) {
      return n1 / n2;
    }
};
